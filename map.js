// 21. map Method()

// map Method Transforms each elements and returns a new array

// Return Value : New Array, Chainable : Yes, Changes Original : Yes, Common Use Case : Create new Array


let numbers = [1, 2, 3, 4, 5]
console.log(numbers); // 12345
let final = numbers.map ((x) => x *2);
console.log(final);  // 246810
console.log(numbers);
